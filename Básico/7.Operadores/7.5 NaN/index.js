// NaN - Not a number
const num1 = 10;
const num2 = 'Victória';
console.log(num1 * num2);

// Quer dizer que sua conta não bateu já que os dados são diferentes.

// Convertendo uma string á um número inteiro 
const num3 = 10;
const num4 = parseInt('5');
console.log(num3 + num4);
console.log(typeof num4);

// A função parseInt('') converte uma string e retorna um número.

// parseFloat
const num5 = 10;
const num6 = parseFloat('8.6');
console.log(num5 + num6);
console.log(typeof num6);

// Podemos usar também a função Number

const num7 = 10;
const num8 = Number('2');
console.log(num7 - num8);
console.log(typeof num8);

const num9 = 10;
const num10 = Number('5.5');
console.log(num9 + num10);
console.log(typeof num10);