// Diferença entre var e let 
// No var quando vamos mudar o valor da variavél precisamos escrever o código exatamente igual.
var nome = 'Victória';
var nome = 'Arkan';

console.log(nome)

// Já no let não podemos escrever o 'let' novamente, apenas o nome da variavel.
let nome1 = 'Victória';
nome1 = 'Arkan';

console.log(nome1)