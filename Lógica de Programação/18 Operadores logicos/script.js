/*
Operadores logicos

&& -> AND -> E
|| -> OR -> OU
! -> NOT

*/

console.log(10 > 5 && 7 < 10); // Retorna true já que 10 é maior que 5 AND 7 é menor que 10.

console.log(10 > 5 || 7 >= 10); // Retorna true já que 10 é maior que 5 OR 7 é maior que 10. Se todas as expressões forem verdadeira o retorno é false.

console.log(!true); // Retorna false
console.log(!!true) // Duas negações retorna para o valor original. true NÃO é verdadeiro logo true é falso. false NÃO é falso logo ele é true.

